package controller.opcoes;

/**
 * Enumerador de opções para o admin.
 * <p>
 * Este enum representa as possíveis ações que um administrador pode realizar.
 */
public enum OpcoesAdmin {
    /**
     * Opção para cadastrar um novo funcionário.
     */
    CADASTRAR_FUNC, 

    /**
     * Opção para listar todos os funcionários.
     */
    LISTAR_FUNC, 

    /**
     * Opção para remover um funcionário.
     */
    REMOVER_FUNC, 

    /**
     * Opção para sair do sistema.
     */
    SAIR;
}