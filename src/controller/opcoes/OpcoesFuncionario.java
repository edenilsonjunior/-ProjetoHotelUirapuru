package controller.opcoes;

/**
 * Enumerador de opções para o funcionário.
 * <p>
 * Este enum representa as possíveis ações que um funcionário pode realizar.
 */
public enum OpcoesFuncionario {
    /**
     * Opção para gerenciar as hospedagens.
     */
    OPCOES_HOSPEDAGEM,

    /**
     * Opção para gerar relatórios.
     */
    RELATORIOS,

    /**
     * Opção para sair do sistema.
     */
    SAIR;
}