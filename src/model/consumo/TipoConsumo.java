package model.consumo;

/**
 * Enumerador de tipos de consumo.
 * <p>
 * Este enum representa os diferentes tipos de consumo que podem ser registrados.
 */
public enum TipoConsumo {
    /**
     * Opção para consumo de serviços de lavanderia.
     */
    LAVANDERIA, 

    /**
     * Opção para consumo de itens do frigobar.
     */
    FRIGOBAR, 

    /**
     * Opção para consumo de serviços do restaurante.
     */
    RESTAURANTE, 

    /**
     * Opção para consumo de serviços de telefone.
     */
    TELEFONE;
}